print('\nExemplo 4: Contagem com Passo (step)\n')

print('A. Contagem de 0 até 7 com passo (step) 2:')
for c in range(0, 7, 2):
    print(c)
print('FIM!\n')

print('B. Contagem:\n - Início: 5\n - Final: 19\n - Passo (step) 3:')
print('=> Início: ', end='')
for c in range(5, 19, 3):
    print(c)
print('FIM!\n')
